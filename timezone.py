from datetime import datetime
import pytz


 
format = "%Z%z"

tzDict = {}
 
# Current time in UTC
now_utc = datetime.now(pytz.timezone('UTC'))
print(now_utc.strftime(format))

for tz in pytz.all_timezones:
    time = now_utc.astimezone(pytz.timezone(tz))
    tz_text = time.strftime(format)
    tzSplit = None
    if '+' in tz_text:
        tzSplit = tz_text.split('+')
        tzSplit.append('+')
    else :
        tzSplit = tz_text.split('-')
        tzSplit.append('-')
    if tz_text not in tzDict:
        tzDict[tz_text]  = tzSplit

    print("timezone: {}, time: {}".format(tz, tz_text))

print(tzDict)

